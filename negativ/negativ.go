// Негативные последствия: В данном коде justString может ссылаться на участок памяти,
// который уже был освобожден после завершения работы createHugeString, что приведет к ошибке доступа к памяти (panic).
// Исправление: Необходимо скопировать подстроку в новую переменную перед освобождением памяти:
var justString string

func someFunc() {
	v := createHugeString(1 << 10)
	justString = v[:100]
	// Копируем в новую переменную
	newString := justString
	_ = newString // используем, чтобы избежать предупреждения "newString declared but not used"
}

func main() {
	someFunc()
}
